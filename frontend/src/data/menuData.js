export const menuImageFor = (item) => {
  const name = item.name.toLowerCase();
  const category = item.category.toLowerCase();

  if (category === "soups" || category === "salads") return "/gallery/web/A6703756.png";
  if (category === "non-veg starters") return "/gallery/web/A6703750.JPG";
  if (category === "veg starters") return "/gallery/web/A6703751.JPG";
  if (category === "non-veg gravies" || category === "mutton curries" || category === "veg curries") return "/gallery/web/A6703752.JPG";
  if (category === "rice bowls" || category === "rice items" || category === "noodles") return "/gallery/web/A6703753.JPG";
  if (category === "indian breads") return "/gallery/web/A6703749.JPG";
  if (category === "arabian mandi") return "/gallery/web/A6703754.JPG";
  if (["desserts", "ice creams", "lassi", "mocktails", "milkshakes", "faloodas"].includes(category)) return "/gallery/web/A6703755.JPG";
  if (name.includes("biryani")) return name.includes("dum") ? "/gallery/web/A6703658.webp" : "/gallery/web/A6703742.webp";
  if (category === "zaffran grill" || name.includes("grill") || name.includes("tandoori chicken")) return "/gallery/web/A6703666.webp";
  return "";
};

